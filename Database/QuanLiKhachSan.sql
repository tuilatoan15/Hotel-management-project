Create database LINK;
drop table Phong
CREATE TABLE Phong (
    SoPhong INT PRIMARY KEY,
	NgayTra datetime,
    TenPhong NVARCHAR(255),
    Gia Float ,
	Trangthai int,
	check (TrangThai BETWEEN 0 AND 2)
);


CREATE TABLE Khach(
	MaKhach VARCHAR(10) PRIMARY KEY,
	HoTen NVARCHAR(50) NOT NULL,
	Tuoi INT NOT NULL,
	GioiTinh NVARCHAR(10) NOT NULL,
	SDT VARCHAR(10) NOT NULL,
	CCCD VARCHAR(12) NOT NULL,
	CONSTRAINT CHK_GT CHECK (GIOITINH IN (N'Nam', N'Nữ')),
);

CREATE TABLE ThongTinThuePhong (
    SoHD INT IDENTITY(1,1) PRIMARY KEY,
	MaKhach char(10),
    Phong INT,
    HoTen NVARCHAR(100),
    Tuoi INT,
    GioiTinh NVARCHAR(10),
    SDT VARCHAR(20),
    CCCD VARCHAR(20) ,
    NgayThue DATETIME,
    NgayTra DATETIME,
	TongGioThue int,					
	GiaPhong int,	
	TongTienPhong FLOAT,
    TienDatCoc FLOAT,
	TongTienDichVu float,
	TongBuill float
	);
	
CREATE TABLE ThongTinSuDungDichVu (
	STT INT IDENTITY(1,1) PRIMARY KEY,
    Phong INT,
    SoHD INT,
    TenLoaiDichVu NVARCHAR(250),
    SoLuong INT,
    NgaySuDung DATETIME,
    FOREIGN KEY (SoHD) REFERENCES ThongTinThuePhong(SOHD),
    CHECK (SoLuong > 0)
);	

CREATE TABLE DichVu (
	MaDichVu NVARCHAR(10) PRIMARY KEY,
	TenLoaiDichVu NVARCHAR(255) NOT NULL,
	TenDichVu NVARCHAR(255) NOT NULL,
	DonGia FLOAT NULL,
	CHECK (DonGia > 0)
)
 
 CREATE TABLE NguoiDung (
    TenDangNhap VARCHAR(50) NOT NULL PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    SDT CHAR(10) NOT NULL,
    MatKhau VARCHAR(16) NOT NULL,
    CONSTRAINT chk_SDT CHECK (SDT LIKE '0%' AND LEN(SDT) = 10)
);

